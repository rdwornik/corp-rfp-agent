REQUIRED_HEADERS = ["Question", "Answer", "Action"]


def validate_excel(path: Path) -> tuple[bool, str, dict]:
    """Validate Excel structure. Returns (ok, error_msg, metadata)."""
    from openpyxl import load_workbook

    try:
        wb = load_workbook(str(path), read_only=True, data_only=True)
    except Exception as e:
        return False, f"Cannot open Excel: {e}", {}

    # Check sheets
    if "Questions & Answers" not in wb.sheetnames:
        return False, "Missing sheet 'Questions & Answers'", {}

    # Read metadata
    metadata = {}
    if "_metadata" in wb.sheetnames:
        ws_meta = wb["_metadata"]
        for row in ws_meta.iter_rows(min_row=1, max_col=2, values_only=True):
            if row[0] and row[1]:
                metadata[str(row[0])] = str(row[1])

    # Check headers
    ws = wb["Questions & Answers"]
    header_row = [cell.value for cell in next(ws.iter_rows(min_row=1, max_row=1))]
    for required in REQUIRED_HEADERS:
        if required not in header_row:
            return False, f"Missing column '{required}'", metadata

    wb.close()
    return True, "", metadata


def parse_rows(path: Path) -> list[dict]:
    """Parse all data rows from the Excel pack."""
    from openpyxl import load_workbook

    wb = load_workbook(str(path), read_only=True, data_only=True)
    ws = wb["Questions & Answers"]

    # Build column index from header
    header_row = [cell.value for cell in next(ws.iter_rows(min_row=1, max_row=1))]
    col_map = {name: idx for idx, name in enumerate(header_row) if name}

    rows = []
    for row_num, row in enumerate(ws.iter_rows(min_row=2, values_only=True), 2):
        def get(col_name):
            idx = col_map.get(col_name)
            if idx is not None and idx < len(row):
                return row[idx]
            return None

        action = str(get("Action") or "").strip().upper()
        if not action or action == "SKIP":
            rows.append({"row": row_num, "action": "SKIP", "skipped": True})
            continue

        entry_ids = []
        entry_ids_json = get("_entry_ids_json")
        if entry_ids_json:
            try:
                entry_ids = json.loads(str(entry_ids_json))
            except (json.JSONDecodeError, TypeError):
                pass

        if not entry_ids:
            # Fallback: parse from "KB Entries Used" column
            kb_used = str(get("KB Entries Used") or "")
            if kb_used:
                entry_ids = [e.strip() for e in kb_used.split(",") if e.strip()]

        rows.append({
            "row": row_num,
            "question": str(get("Question") or ""),
            "topic": str(get("Topic") or ""),
            "category": str(get("Category") or "technical"),
            "answer": str(get("Answer") or ""),
            "entry_ids": entry_ids,
            "confidence": float(get("Confidence") or 0),
            "action": action,
            "feedback": str(get("Feedback") or ""),
            "robs_answer": str(get("Rob's Answer") or ""),
            "revision_hash": str(get("_revision_hash") or ""),
            "skipped": False,
        })

    wb.close()
    return rows


def parse_general_feedback(path: Path) -> list[dict]:
    """Parse general feedback sheet."""
    from openpyxl import load_workbook

    wb = load_workbook(str(path), read_only=True, data_only=True)
    if "General Feedback" not in wb.sheetnames:
        wb.close()
        return []

    ws = wb["General Feedback"]
    items = []
    for row in ws.iter_rows(min_row=2, values_only=True):
        if len(row) >= 2 and row[1]:
            text = str(row[1]).strip()
            # Skip placeholder text
            if text.startswith("(") and text.endswith(")"):
                continue
            if not text:
                continue
            scope = str(row[2]).strip() if len(row) > 2 and row[2] else "general"
            items.append({"feedback": text, "scope": scope})

    wb.close()
    return items


# ---------------------------------------------------------------------------
# Execution engine
# ---------------------------------------------------------------------------

def execute_row(row: dict, family: str, dry_run: bool = True,
                model: str = "gemini-flash") -> dict:
    """Execute one row's action via the rfp_feedback.py backend.

    Returns result dict with status and details.
    """
    from rfp_feedback import (
        cmd_approve, cmd_reject, cmd_correct_offline, cmd_correct,
        check_forbidden_claims, load_profile, find_entry, load_entry,
        save_entry, append_feedback_log, find_entry_dir,
    )

    action = row["action"]
    entry_ids = row.get("entry_ids", [])
    feedback = row.get("feedback", "")
    robs_answer = row.get("robs_answer", "")
    revision_hash = row.get("revision_hash", "")

    result = {
        "row": row["row"],
        "action": action,
        "status": "pending",
        "detail": "",
    }

    # Stale detection
    if entry_ids and revision_hash:
        if check_stale(entry_ids, revision_hash):
            result["status"] = "CONFLICT"
            result["detail"] = "Entry changed since pack was generated"
            return result

    # Determine answer text to validate
    answer_text = robs_answer if robs_answer else row.get("answer", "")

    # Profile validation for APPROVE and UPDATE
    if action in ("APPROVE", "UPDATE", "NEW"):
        profile = load_profile(family)
        if answer_text:
            violations = check_forbidden_claims(answer_text, profile)
            if violations:
                result["status"] = "QUARANTINED"
                result["detail"] = f"Forbidden claim: {violations[0]}"
                return result

    if dry_run:
        result["status"] = f"WOULD_{action}"
        return result

    # Execute
    if action == "APPROVE":
        if not entry_ids:
            result["status"] = "SKIPPED"
            result["detail"] = "No entry IDs to approve"
            return result
        for eid in entry_ids:
            path, dir_type = find_entry_dir(eid)
            if path and dir_type == "drafts":
                ret = cmd_approve(eid)
                if ret == 0:
                    result["status"] = "APPROVED"
                    result["detail"] = f"Approved {eid}"
                else:
                    result["status"] = "ERROR"
                    result["detail"] = f"Failed to approve {eid}"
                break
        else:
            # Entry exists but not in drafts — just mark as ok
            result["status"] = "APPROVED"
            result["detail"] = "Already verified"

    elif action == "UPDATE":
        if not entry_ids:
            result["status"] = "SKIPPED"
            result["detail"] = "No entry IDs to update"
            return result
        eid = entry_ids[0]
        if robs_answer:
            ret = cmd_correct_offline(eid, robs_answer, dry_run=False)
            result["parse_method"] = "direct"
        else:
            correction = feedback or "Update per review feedback"
            ret = cmd_correct(eid, correction, dry_run=False, model=model)
            result["parse_method"] = "llm"
        result["status"] = "UPDATED" if ret == 0 else "ERROR"
        result["detail"] = f"Updated {eid}"

    elif action == "REJECT":
        if not entry_ids:
            result["status"] = "SKIPPED"
            result["detail"] = "No entry IDs to reject"
            return result
        eid = entry_ids[0]
        reason = feedback or "Rejected via review pack"
        path, dir_type = find_entry_dir(eid)
        if path and dir_type == "drafts":
            ret = cmd_reject(eid, reason)
            result["status"] = "REJECTED" if ret == 0 else "ERROR"
            result["detail"] = f"Rejected {eid}: {reason[:60]}"
        else:
            result["status"] = "SKIPPED"
            result["detail"] = f"{eid} not in drafts"

    elif action == "NEW":
        answer = robs_answer or row.get("answer", "")
        if not answer or answer == "No answer found in KB":
            result["status"] = "SKIPPED"
            result["detail"] = "No answer text for new entry"
            return result

        # Create draft entry
        draft = _create_draft(
            question=row["question"],
            answer=answer,
            family=family,
            category=row.get("category", "technical"),
            topic=row.get("topic", ""),
            feedback=feedback,
        )
        result["status"] = "CREATED"
        result["detail"] = f"Created {draft['id']}"

    else:
        result["status"] = "SKIPPED"
        result["detail"] = f"Unknown action: {action}"

    return result


def _create_draft(question: str, answer: str, family: str,
                  category: str = "technical", topic: str = "",
                  feedback: str = "") -> dict:
    """Create a new draft entry from review pack feedback."""
    from rfp_feedback import save_entry, append_feedback_log

    # Find next draft ID
    family_dir = DRAFTS_DIR / family
    family_dir.mkdir(parents=True, exist_ok=True)
    existing = list(family_dir.glob("KB_DRAFT_*.json"))
    max_id = 0
    for f in existing:
        m = re.search(r'KB_DRAFT_(\d+)', f.stem)
        if m:
            max_id = max(max_id, int(m.group(1)))
    next_id = max_id + 1

    entry_id = f"KB_DRAFT_{next_id:04d}"
    now_iso = datetime.now().strftime("%Y-%m-%dT%H:%M:%S")
    today = datetime.now().strftime("%Y-%m-%d")

    entry = {
        "id": entry_id,
        "question": question,
        "answer": answer,
        "question_variants": [],
        "solution_codes": [],
        "family_code": family,
        "category": category,
        "subcategory": topic,
        "tags": [],
        "confidence": "draft",
        "source_rfps": [],
        "last_updated": today,
        "cloud_native_only": False,
        "notes": f"Created from review pack. Feedback: {feedback[:200]}",
        "generated_by": "import_review_pack.py",
        "generated_at": now_iso,
        "feedback_history": [{
            "action": "created_from_review_pack",
            "timestamp": now_iso,
        }],
    }

    path = family_dir / f"{entry_id}.json"
    save_entry(entry, path)

    append_feedback_log({
        "action": "new_from_review_pack",
        "entry_id": entry_id,
        "family": family,
        "question": question[:80],
    })

    return entry


