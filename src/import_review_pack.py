"""Review Pack Importer -- reads reviewed Excel, applies feedback via backend.

Reads Excel review packs generated by generate_review_pack.py, parses
Rob's feedback (PL/EN bilingual), and executes via rfp_feedback.py backend.

Usage:
  python src/import_review_pack.py path/to/PACK.xlsx --dry-run
  python src/import_review_pack.py path/to/PACK.xlsx --apply
  python src/import_review_pack.py path/to/PACK.xlsx --apply --force
"""

import argparse
