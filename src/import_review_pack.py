def main():
    parser = argparse.ArgumentParser(
        description="Import Review Pack -- apply Excel feedback to KB",
    )
    parser.add_argument("excel", type=str, help="Path to review pack .xlsx")
    parser.add_argument("--dry-run", action="store_true", default=True,
                        help="Preview changes (default)")
    parser.add_argument("--apply", action="store_true",
                        help="Actually apply changes")
    parser.add_argument("--force", action="store_true",
                        help="Allow re-import of processed pack")
    parser.add_argument("--model", default="gemini-flash",
                        help="LLM model for feedback parsing (default: gemini-flash)")
    args = parser.parse_args()

    excel_path = Path(args.excel)
    if not excel_path.exists():
        print(f"[ERROR] File not found: {excel_path}")
        return 1

    dry_run = not args.apply
    import_pack(excel_path, dry_run=dry_run, force=args.force, model=args.model)
    return 0


if __name__ == "__main__":
    sys.exit(main() or 0)
