import json
import os
import re
import sys
from datetime import datetime
from pathlib import Path
from typing import Optional

try:
    from dotenv import load_dotenv
    load_dotenv(override=False)
except ImportError:
    pass

PROJECT_ROOT = Path(__file__).resolve().parents[1]
KB_DIR = PROJECT_ROOT / "data" / "kb"
VERIFIED_DIR = KB_DIR / "verified"
DRAFTS_DIR = KB_DIR / "drafts"
PROFILES_DIR = PROJECT_ROOT / "config" / "product_profiles" / "_effective"
PROCESSED_PACKS = KB_DIR / "processed_packs.jsonl"
GENERAL_FEEDBACK_QUEUE = KB_DIR / "general_feedback_queue.jsonl"


# ---------------------------------------------------------------------------
# Bilingual feedback parsing
# ---------------------------------------------------------------------------

FEEDBACK_PATTERNS = {
    # Polish
    "brakuje": "add_detail",
    "zle": "reject", "nieprawda": "reject", "nieaktualne": "reject",
    "dotyczy": "retag", "popraw": "update", "dodaj": "add_detail",
    "usun": "remove_detail",
    # English
    "missing": "add_detail", "wrong": "reject", "outdated": "reject",
    "incorrect": "reject", "add": "add_detail", "remove": "remove_detail",
    "should mention": "add_detail", "applies to": "retag",
    "ok": "approve", "good": "approve", "correct": "approve",
    "dobrze": "approve", "ok": "approve",
}

# Map parsed intent to import action
INTENT_TO_ACTION = {
    "approve": "APPROVE",
    "reject": "REJECT",
    "update": "UPDATE",
    "add_detail": "UPDATE",
    "remove_detail": "UPDATE",
    "retag": "UPDATE",
}


def parse_feedback_rules(feedback: str) -> Optional[str]:
    """Parse feedback text using bilingual keyword lexicon.

    Returns intent string or None if no match.
    """
    if not feedback or not feedback.strip():
        return None
    text = feedback.lower().strip()
    for pattern, intent in FEEDBACK_PATTERNS.items():
        if pattern in text:
            return intent
    return None


def parse_feedback_llm(feedback: str, question: str,
                       model: str = "gemini-flash") -> str:
    """LLM fallback for feedback parsing."""
    from kb_extract_historical import call_llm

    prompt = f"""Parse this RFP review feedback into an action.

QUESTION: {question}
FEEDBACK (may be in Polish or English): {feedback}

Return EXACTLY one word: APPROVE, UPDATE, REJECT, or SKIP
"""
    raw = call_llm(prompt, model=model)
    result = raw.strip().upper()
    if result in ("APPROVE", "UPDATE", "REJECT", "SKIP"):
        return result
    return "SKIP"


# ---------------------------------------------------------------------------
# Stale detection
# ---------------------------------------------------------------------------

def _content_hash(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()[:16]


def check_stale(entry_ids: list[str], revision_hash: str) -> bool:
    """Check if any referenced entry has changed since pack generation.

    Returns True if stale (hash mismatch).
    """
    if not revision_hash or not entry_ids:
        return False

    from rfp_feedback import find_entry, load_entry

    for eid in entry_ids:
        path = find_entry(eid)
        if path:
            entry = load_entry(path)
            current_hash = _content_hash(entry.get("answer", ""))
            if current_hash != revision_hash:
                return True
    return False


# ---------------------------------------------------------------------------
# Pack tracking
# ---------------------------------------------------------------------------

def is_already_processed(pack_id: str) -> bool:
    """Check if pack_id has been processed before."""
    if not PROCESSED_PACKS.exists():
        return False
    for line in PROCESSED_PACKS.read_text(encoding="utf-8").strip().split("\n"):
        if not line.strip():
            continue
        try:
            record = json.loads(line)
            if record.get("pack_id") == pack_id:
                return True
        except json.JSONDecodeError:
            continue
    return False


def mark_processed(pack_id: str, file_path: str, summary: dict) -> None:
    """Record pack as processed."""
    PROCESSED_PACKS.parent.mkdir(parents=True, exist_ok=True)
    record = {
        "pack_id": pack_id,
        "file": file_path,
        "processed_at": datetime.now().strftime("%Y-%m-%dT%H:%M:%S"),
        "summary": summary,
    }
    with open(PROCESSED_PACKS, "a", encoding="utf-8") as f:
        f.write(json.dumps(record, ensure_ascii=False) + "\n")


