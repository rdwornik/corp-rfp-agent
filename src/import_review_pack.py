def import_pack(excel_path: Path, dry_run: bool = True,
                force: bool = False,
                model: str = "gemini-flash") -> dict:
    """Full import pipeline. Returns summary dict."""
    summary = {
        "approved": 0, "updated": 0, "rejected": 0, "new": 0,
        "skipped": 0, "conflicts": 0, "quarantined": 0, "errors": 0,
        "total": 0, "actionable": 0,
        "update_rule_parsed": 0, "update_llm_parsed": 0,
        "general_feedback": 0,
    }

    # Step 1: Validate
    print(f"[Step 1] Validating {excel_path.name}...")
    ok, error, metadata = validate_excel(excel_path)
    if not ok:
        print(f"[ERROR] {error}")
        return summary

    pack_id = metadata.get("pack_id", excel_path.stem)
    family = metadata.get("family", "unknown")
    print(f"  Pack: {pack_id}, Family: {family}")

    if not force and is_already_processed(pack_id):
        print(f"[ERROR] Pack {pack_id} already processed. Use --force to re-import.")
        return summary

    # Step 2: Parse rows
    print(f"[Step 2] Parsing rows...")
    rows = parse_rows(excel_path)
    summary["total"] = len(rows)
    actionable = [r for r in rows if not r.get("skipped")]
    summary["actionable"] = len(actionable)
    print(f"  Total: {len(rows)}, Actionable: {len(actionable)}")

    # Step 3-6: Execute each row
    print(f"[Step 3-6] {'Previewing' if dry_run else 'Executing'} actions...")
    results = []
    for row in rows:
        if row.get("skipped"):
            summary["skipped"] += 1
            results.append({"row": row["row"], "action": "SKIP", "status": "SKIPPED"})
            continue

        # Step 4: Parse feedback if action seems underspecified
        if row["action"] in ("UPDATE",) and row.get("feedback"):
            intent = parse_feedback_rules(row["feedback"])
            if intent:
                mapped = INTENT_TO_ACTION.get(intent, row["action"])
                if mapped != row["action"]:
                    row["action"] = mapped

        result = execute_row(row, family, dry_run=dry_run, model=model)
        results.append(result)

        status = result["status"]
        if status in ("APPROVED", "WOULD_APPROVE"):
            summary["approved"] += 1
        elif status in ("UPDATED", "WOULD_UPDATE"):
            summary["updated"] += 1
            if result.get("parse_method") == "direct":
                summary["update_rule_parsed"] += 1
            else:
                summary["update_llm_parsed"] += 1
        elif status in ("REJECTED", "WOULD_REJECT"):
            summary["rejected"] += 1
        elif status in ("CREATED", "WOULD_NEW"):
            summary["new"] += 1
        elif status == "CONFLICT":
            summary["conflicts"] += 1
        elif status == "QUARANTINED":
            summary["quarantined"] += 1
        elif status == "ERROR":
            summary["errors"] += 1
        else:
            summary["skipped"] += 1

    # Step 7: General feedback
    print(f"[Step 7] Processing general feedback...")
    general = parse_general_feedback(excel_path)
    if general and not dry_run:
        GENERAL_FEEDBACK_QUEUE.parent.mkdir(parents=True, exist_ok=True)
        with open(GENERAL_FEEDBACK_QUEUE, "a", encoding="utf-8") as f:
            for item in general:
                item["pack_id"] = pack_id
                item["family"] = family
                item["timestamp"] = datetime.now().strftime("%Y-%m-%dT%H:%M:%S")
                f.write(json.dumps(item, ensure_ascii=False) + "\n")
    summary["general_feedback"] = len(general)
    if general:
        print(f"  {len(general)} ticket(s) {'queued' if not dry_run else 'found'}")

    # Step 8: Report + track
    print_import_report(pack_id, family, summary, results, dry_run)

    if not dry_run:
        mark_processed(pack_id, str(excel_path), summary)
        print(f"  Pack marked as processed.")

    return summary


# ---------------------------------------------------------------------------
# Report
# ---------------------------------------------------------------------------

def print_import_report(pack_id: str, family: str, summary: dict,
                        results: list[dict], dry_run: bool) -> None:
    """Print formatted import report."""
    mode = "DRY RUN" if dry_run else "APPLIED"
    border = "=" * 60

    print(f"\n{border}")
    print(f"  Review Pack Import Report [{mode}]")
    print(f"{border}")
    print(f"  Pack: {pack_id} | Family: {family}")
    print(f"  Rows: {summary['total']} total, "
          f"{summary['actionable']} actionable")
    print()
    print(f"  APPROVED:     {summary['approved']:>3}")

    update_detail = ""
    if summary["updated"]:
        parts = []
        if summary["update_rule_parsed"]:
            parts.append(f"{summary['update_rule_parsed']} direct")
        if summary["update_llm_parsed"]:
            parts.append(f"{summary['update_llm_parsed']} LLM-parsed")
        if parts:
            update_detail = f" ({', '.join(parts)})"
    print(f"  UPDATED:      {summary['updated']:>3}{update_detail}")

    print(f"  REJECTED:     {summary['rejected']:>3}")
    print(f"  NEW:          {summary['new']:>3}")
    print(f"  SKIPPED:      {summary['skipped']:>3}")
    if summary["conflicts"]:
        print(f"  CONFLICTS:    {summary['conflicts']:>3} (stale entries)")
    if summary["quarantined"]:
        print(f"  QUARANTINED:  {summary['quarantined']:>3} (forbidden claim violation)")
    if summary["errors"]:
        print(f"  ERRORS:       {summary['errors']:>3}")
    if summary["general_feedback"]:
        print(f"\n  General Feedback: {summary['general_feedback']} ticket(s) "
              f"{'queued' if not dry_run else 'found'}")
    print(f"{border}")

    # Detail per non-skip result
    details = [r for r in results if r.get("status") not in ("SKIPPED", None)]
    if details:
        print(f"\n  Details:")
        for r in details[:20]:
            print(f"    Row {r.get('row', '?')}: {r.get('action', '?')} -> "
                  f"{r.get('status', '?')} {r.get('detail', '')[:50]}")
        if len(details) > 20:
            print(f"    ... and {len(details) - 20} more")


